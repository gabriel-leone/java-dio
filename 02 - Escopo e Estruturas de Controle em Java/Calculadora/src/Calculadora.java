public class Calculadora {

  /**
   * Esse método é responsável por somar dois números inteiros.
   * 
   * @param num1 este é o primeiro parâmetro e representa o primeiro número a ser
   *             somado.
   * @param num2 este é o segundo parâmetro e representa o primeiro número a ser
   *             somado.
   * @return este método retorna a soma dos dois números.
   */
  public int somar(int num1, int num2) {
    return num1 + num2;
  }

  /**
   * Esse método é responsável por subtrair dois números inteiros.
   * 
   * @param num1 este é o primeiro parâmetro e representa o minuendo.
   * @param num2 este é o segundo parâmetro e representa o subtraendo.
   * @return este método retorna a subtração dos dois números.
   */

  public int subtrair(int num1, int num2) {
    return num1 - num2;
  }

}
